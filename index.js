const Addition = (num1, num2) => {
  return num1 + num2;
};

const Subtract = (num1, num2) => {
  return num1 - num2;
};

const Multiplay = (num1, num2) => {
  return num1 * num2;
};

const Division = (num1, num2) => {
  return num1 / num2;
};

module.exports = {
  Addition,
  Subtract,
  Multiplay,
  Division,
};
